package week1.day2;

public class LearnOperators {

	public static void main(String[] args) {
	//Arithmetic Operations
		int a=10;
		int b=5;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a%b);
		//Assignment operators
		int result = 100;
		a+=5;
		System.out.println(a);
		//Comparison operator
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a<b);
		System.out.println(a>b);
		//Logical Operators
		System.out.println((a>=b)&&(a>b));
		int i =10;
		System.out.println(i++);
		System.out.println(i);

	}

}
